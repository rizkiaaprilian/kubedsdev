	<h3>REPAIR PLANNING SOP</h3>
	<p>When the vehicle has been sufficiently disassembled to gain visual access to all damage, and pictures of all damaged areas have been taken, the Estimator writes a meticulous repair plan adhering to applicable insurance company guidelines. Roles and responsibilities for the following tasks should be defined: drive-in estimates, tow-ins, drop-offs and possible total losses. Please note, possible totals should be scrutinized if at all possible to prevent unnecessary disassembly.</p>
	<div class="text-center"><?=\Asset::img('planning-sop.jpg', array('alt' => 'Planning SOP'));?></div>
	<p>Both the body and paint technician should be utilized to review the estimate for complete accuracy (parts, labor times and procedures, sublet activities, materials, fluids, blends and color match) before locking.</p>
	<h5 class="text-center">Important Note: There cannot be any hidden damage remaining after this stage is complete.<br><span class="txt-red">ZERO supplements and 100% accuracy is the goal.</span></h5>
	<hr>
	<h4>Process Steps</h4>
	<ol>
		<li>Place call to OE vendor requesting parts blow-ups / illustrations for each damaged area of the vehicle. The parts illustrations will be used during the Repair Planning stage; requesting the information in advance will provide some lead time to the vendor and help ensure the diagrams are available when we need them</li>
		<li>Systematically inspect the vehicle, itemizing all operations necessary to repair the vehicle</li>
		<li>Focus on thoroughly specifying all parts, materials, and sublet operations needed
			<ul>
				<li>Determine the type of part to be used as well: OE, aftermarket or used</li>
				<li>Capture all fasteners, decals, emblems, labels, seam sealers, painted pinstripes or stripe tape, sound deadening pads, etc.</li>
				<li>Document internal or external glass work, key coding, mechanical repairs, etc.</li>
				<li>Add special materials and minor parts</li>
				<li>Focus on any items, that if missed, would cause a delay
					<div class="text-center"><?=\Asset::img('planning-sop2.jpg', array('alt' => 'Planning SOP'));?></div>
				</li>
			</ul>
		</li>
		<li>Utilize all available reference materials to capture every applicable operation
			<ul>
				<li>P-pages</li>
				<li>Alldata</li>
				<li>Parts code tables</li>
				<li>Vehicle manufacturer repair specifications</li>
				<li>Refinish procedures</li>
			</ul>
			NOTE: remember that at this point, it’s not about whether you’ll be paid for every operation that is written down, it's about capturing all the operations that are truly necessary to repair the vehicle. If an 'unallowable' operation is identified later, the procedure can simply be zeroed out. Capturing all operations is critical at this stage to clearly communicate the repair plan to the technical staff.
		</li>
		<li>Paint analysis
		<ul>
			<li>Repair planner and paint team leader collaborate on the refinish requirements for the repair</li>
			<li>Determine the paint code, color match / variance, and blend areas</li>
			<li>Identify whether a three stage finish is on the vehicle, add it to the repair plan, and proceed accordingly</li>
			<li>Determine if a spray out is required prior to refinishing</li>
			<li>Determine if parts will be refinished on or off the vehicle
			<ul>
				<li>Painting parts off the vehicle allows for the possibility of multiple repair orders to be refinished at one time, increasing production in your facility.</li>
			</ul>
			<li>Ensure required materials (including special orders if applicable) will be in stock when the vehicle reaches the paint department. Be sure to bill for additional materials if specialty items are needed</li>
		</ul>
		<li>Review the Repair Plan with the 'advisory team' consisting of the Production Coordinator, Parts Specialist and Lead Body Technician to gain different perspectives
			<div class="text-center"><?=\Asset::img('planning-sop3.jpg', array('alt' => 'Planning SOP'));?></div>
			<ul>
				<li>Review the overall plan, with the advisory team peppering you with questions: "did you include&hellip;", "what have you planned here&hellip;", etc.</li>
				<li>Discuss repair versus replace decisions</li>
				<li>This should just be a brief, few minute review</li>
			</ul>
		</li>
		<li>Repair Planner completes the "Final Estimate" row on the Vehicle Intake Tracking Sheet. If the Repair Planner is not the person ordering parts, the Tracking Sheet should be removed from the vehicle and transferred to the Parts Coordinator.</li>
	</ol>
	<hr>
	<h4>Important Notes</h4>
	<ul>
		<li>There cannot be any hidden damage remaining after this stage is complete. ZERO supplements and 100% accuracy is the goal.
		<li>Accuracy should be tracked by measuring 'Supplements After Disassembly' (the number of supplements written after a complete Disassembly has been performed and Repair Plan has been written)</li>
		<li>After Disassembly an accurate final estimate should be finished and a complete parts order placed within ONE hour.</li>
		<li>An expedited vehicle intake, mapping, and Disassembly are critical to reducing cycle time and overall chaos. Each task should be measured separately if they cannot be completed within four hours of drop-off.</li>
	</ul>
