<div class="column">
	<h3>VEHICLE STAGING FOR REPAIR SOP</h3>
	<p>Once the estimate has been completed the estimator will release the vehicle. The Disassembly tech will weatherproof the vehicle and park in the "waiting for parts" area and the parts carts will be moved to the parts department by the disassembly tech. The tech then takes an empty cart back to disassembly area if one is available. The vehicle will remain parked until the complete parts order has been received. At this time the vehicle will be dispatched for repair.</p>
	<p>Vehicles that require no parts will be dispatched to the first available technician.</p>
	<hr>
	<h4>Process Steps</h4>
	<ol>
		<li>Disassembly Technician ensures the hardware/small item storage item is loaded on the parts cart and all items on the cart are secured</li>
		<li>If needed, Technician weatherproofs the vehicle by covering and sealing windows with plastic crash wrap and tape</li>
		<li>If work can constructively progress prior to parts arrival, Technician moves the vehicle to the assigned repair bay</li>
		<li>If work must be delayed until certain parts arrive, move the vehicle to Waiting For Parts staging area</li>
		<li>If work must be delayed until a technician is available, move the vehicle to the Ready For Repair staging area</li>
	</ol>
	<h4>When Parts Arrive</h4>
	<p>When parts arrive, mirror match and place on cart as soon as possible. If all parts have been received,</p>
	<ul>
		<li>Notify production coordinator</li>
		<li>Place parts cart in a 'ready for production' staging area
			<div class="text-center"><?=\Asset::img('staging-sop.jpg', array('alt' => 'Staging SOP'));?></div>
		</li>
	</ul>
	<hr>
	<h4>Important Notes</h4>
	<ul>
		<li>Processing time is critical to success</li>
		<li>Downtime must be minimized; there are three critical areas in which downtime can be minimized:
		<ul>
			<li>Vehicle dropped to disassembly / repair plan</li>
			<li>Repair plan to parts ordered</li>
			<li>Parts arrive to vehicle in production</li>
			<li>Must have a sense of urgency</li>
		</ul>
		<li>Pre-Production best practice criteria include:
			<ul>
				<li>Vehicle is prewashed</li>
				<li>Authorizations have been received</li>
				<li>Vehicle is 100% disassembled</li>
				<li>Necessary parts illustrations (Alldata) procedures for structural, mechanical and electrical repairs are with the vehicle</li>
				<li>The file is compliant to facility standards (photos, DRP agreement, admin)</li>
				<li>All "critical" parts have been received and mirror matched prior to dispatch</li>
				<li>Tech is ready to work on the vehicle</li>
			</ul>
		</li>
	</ul>
</div>