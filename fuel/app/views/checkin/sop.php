	<h3>Check-In Processes</h3>
	<p>The success of today’s collision center relies on fast process times. The first critical step is vehicle intake. When a damaged vehicle arrives for repair, the teams focus must be to onboard the customer, move the vehicle through to Disassembly and place a complete parts order in the shortest amount of time possible.</p>
	<hr>
	<p><strong>Process Steps</strong></p>
	<ol>
		<li>CSR or Salesperson or other team member welcomes the customer to our facility.</li>
		<li>Create the RO jacket with key tag and mirror tag.</li>
		<li>Obtain the keys from the vehicle owner</li>
		<li>Tag the keys with the RO number, customer last name and vehicle color, year, make, and model</li>
		<li>CSR completes the "Repair Order #" as well as the "Date" and "Started" fields on the "Drop Off" row on the Vehicle Intake Tracking Sheet and places the sheet on the dash with the RO jacket.</li>
		<li>Obtain all required signatures: direction to pay, repair authorization, etc.</li>
		<li>Reconfirm their contact information for updates and obtain all applicable phone numbers as well as email addresses.</li>
		<li>Ask the customer for their preferred method of communication. Promise to contact them if there are any changes.</li>
		<li>Explain our payment policies. Be clear on the customer’s payment responsibilities at delivery (amount of deductible if appropriate, etc.). Also be clear on acceptable forms of payment (specific credit cards accepted, personal checks, etc.).</li>
		<li>Determines whether the customer will need transportation once the check-in process is complete. If so, make sure transportation arrangements are ready as soon as the check-in process is complete.</li>
		<li>Transfer the customer to the Salesperson. Communicate the transportation arrangements to the Salesperson.</li>
		<li>Salesperson reviews the insurance assignment with the customer. Verify and validate the information, paying special attention to: claim information, deductible payment, rental car coverage, and completion date</li>
		<li>Reiterate the customer's "special concerns"</li>
		<li>Invite the customer to walk with you to the vehicle in order to briefly review the damage as needed</li>
		<li>Place copies of the management system 'window sticker' report listing basic RO information (RO number in large bold print, customer name, contact information, insurance company information, target delivery date, etc.) in a highly visible location on the vehicle's dashboard.</li>
		<li>Walk around the entire vehicle with the customer, completing the pre-repair inspection checklist together. Inspect, identify and document prior damage. This is your opportunity to explain the difference between accident related and prior damages.</li>
		<li>UPSELL! Take the time to understand the customer’s expectations and offer additional services and the opportunity to repair prior damage.</li>
		<li>Locate and obtain the digital camera</li>
		<li>Set the camera’s resolution to the lowest available setting.</li>
		<li>Take preliminary photos
		<ul>
			<li>Four corners of the vehicle exterior</li>
			<li>Registration sticker</li>
			<li>Dashboard gauge panel</li>
			<li>Vehicle mileage</li>
			<li>Vehicle interior options</li>
			<li>VIN</li>
			<li>License plate</li>
			<li>Take photos of the damaged area(s)</li>
		</ul>
		<li>Review the repair process with the customer to ensure they understand what will be happening next. If appropriate, explain the repair planning process in layman's terms (avoid industry jargon at all costs!) so they understand there might be additional damage that is uncovered.</li>
		<li>Reconfirm the target delivery date – BE SPECIFIC! – but also be clear that you may be calling with a more solid date once the repair plan has been written.</li>
		<li>Thank the customer for their business and set the expectations for when you'll next be in contact.</li>
		<li>If the customer needed transportation assistance, direct them to the appropriate resource.</li>
		<li>Immediately move the vehicle to the Pre-Wash Staging area to be washed.</li>
		<li>Place the keys on the Pre Wash board in prioritized order (keys on top are highest priority).</li>
	</ol>
